export class ProductsCollectioner {
    public static getMaxPrice(): number {
        let allPrices = productsList.map((product) => product.price)
        let result = Math.max(...allPrices)
        return result
    }

    public static getMinPrice(): number {
        let allPrices = productsList.map((product) => product.price)
        let result = Math.min(...allPrices)
        return result
    }

    public static getAllBrands(): string[] {
        return [...new Set(productsList.map((product) => product.brand))]
    }

    public static getAllColors(): string[] {
        return [...new Set(productsList.map((product) => product.color))]
    }

    public static getAllScreenSizes(): string[] {
        let values = [...new Set(productsList.map((product) => product.screenSize))].sort(
            (a, b) => a - b
        )
        return values.map((value) => String(value))
    }

    public static getAllScreenResolutions(): string[] {
        return [...new Set(productsList.map((product) => product.screenResolution))]
    }

    public static getAllVideoStorages(): string[] {
        let values = [...new Set(productsList.map((product) => product.videoCardRAM))].sort(
            (a, b) => a - b
        )
        return values.map((value) => String(value))
    }
}
